import com.digitalasset.canton.config.*

import java.nio.file.Path

case class AppInstance(
    // A user for the provider which is also auto-onboarded as a wallet user:
    walletUser: String,
    // An optional alternative username for the service backend. Specifying this can be useful if the
    // backend uses a machine-to-machine token, as opposed to a login one used in the wallet user above.
    // If not specified, or equal to the wallet user - no additional user is created.
    serviceUser: Option[String],
    dars: Seq[Path],
)

case class ValidatorOnboardingConfig(
    remoteSv: RemoteSvAppConfig, // onboarding sponsor
    secret: String, // generated by the sponsor's SV app
)
object ValidatorOnboardingConfig {
  def hideConfidential(config: ValidatorOnboardingConfig): ValidatorOnboardingConfig = {
    val hidden = "****"
    config.copy(secret = hidden)
  }
}

case class ValidatorAppBackendConfig(
    override val adminApi: CommunityAdminServerConfig = CommunityAdminServerConfig(),
    override val storage: CommunityStorageConfig = CommunityStorageConfig.Memory(),
    ledgerApiUser: String,
    walletServiceUser: String,
    auth: AuthConfig,
    appInstances: Map[String, AppInstance],
    remoteParticipant: CoinRemoteParticipantConfig,
    remoteScan: ScanAppClientConfig,
    automation: AutomationConfig = AutomationConfig(),
    domains: ValidatorDomainConfig,
    onboarding: Option[ValidatorOnboardingConfig],
) extends LocalCoinConfig // TODO(#736): fork or generalize this trait.
    {
  override val nodeTypeName: String = "validator"

  override def clientAdminApi: ClientConfig = adminApi.clientConfig

}

case class ValidatorAppClientConfig(
    adminApi: CoinHttpClientConfig
) extends RemoteCoinConfig {
  override def clientAdminApi: ClientConfig = adminApi.clientConfig
}
